package mm.lay;

/**
 * @author benno
 * 
 */
public class LatLng {
	protected double lat = 0;
	protected double lon = 0;

	public LatLng(final double lat, final double lon) {
		super();
		this.lat = lat;
		this.lon = lon;
	}

	/**
	 * @return the lat
	 */
	public double getLat() {
		return lat;
	}

	/**
	 * @param lat
	 *            the lat to set
	 */
	public void setLat(final double lat) {
		this.lat = lat;
	}

	/**
	 * @return the lon
	 */
	public double getLon() {
		return lon;
	}

	/**
	 * @param lon
	 *            the lon to set
	 */
	public void setLon(final double lon) {
		this.lon = lon;
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return "LatLng [lat=" + lat + ", lon=" + lon + "]";
	}
}
